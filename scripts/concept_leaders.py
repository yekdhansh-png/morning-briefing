def lookup_leaders_by_concept(concept: str) -> list[dict]:
    """
    根据 concept 关键字模糊查找龙头股池。
    返回 [{name, code}, ...]，找不到返回 []
    """
    if not concept:
        return []

    # 先直接命中
    if concept in CONCEPT_LEADERS:
        return CONCEPT_LEADERS[concept]

    # 用别名表
    for canonical, aliases in CONCEPT_ALIASES.items():
        for alias in aliases:
            if alias in concept or concept in alias:
                if canonical in CONCEPT_LEADERS:
                    return CONCEPT_LEADERS[canonical]

    # 模糊：concept 是否是任一 key 的子串/反之
    for key, leaders in CONCEPT_LEADERS.items():
        if key in concept or concept in key:
            return leaders

    return []


